typedef struct{
  double first;
  int    second;
}pdi;
pdi*mkpdi(double a, int b){
  pdi*rv=calloc(1, sizeof(pdi));
  rv->first =a;
  rv->second=b;
  return rv;
}
int cmpPdi(const void*pa, const void*pb){
  pdi*a=(pdi*)pa;
  pdi*b=(pdi*)pb;
  if(a->first  != b->first) return(a->first  < b->first )?-1:1;
  if(a->second != b->second)return(a->second < b->second)?-1:1;
  return 0;
}
